package study.viradonojiraya._legacy.days.review.domain.person;

import study.viradonojiraya._legacy.days.review.domain.interfaces.Information;

public abstract class Person implements Information {
    protected String name;
    protected int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
